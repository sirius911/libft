#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*str;
	size_t	i;

	i = 0;
	if (n > 0)
	{
		str = (char*)s;
		while (i < n)
		{
			str[i] = 0;
			i++;
		}
	}
}
